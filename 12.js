//Percabangan If.. Else..

//mengisi variabel tahun dengan data numerik
let tahun = 2020

//kondisi else hanya akan dieksekusi bila kondisi if bernilai false
if(tahun % 4 == 0){
    console.log(tahun + " adalah tahun kabisat");
}else{
    console.log(tahun + " bukan tahun kabisat");
}
/*karena 2020 dibagi 4 sisa 0 dan 
statement if bernilai true maka kondisi else tidak dieksekusi*/